# A maneira mais utilizada para formatar uma strings, ou seja, usar variáveis dentro dela. É com o f-string.
# Exemplo

nome = "Charles Albert"
curso = "Python"

print(f"Seja muito bem vindo, senhor: {nome}")
print(f"ao curso de {curso}")

# Para pegar informações de um usuario, use: input()
nome = input("Qual o seu nome? ")
print(f"Seja muito bem vindo, senhor: {nome}")

