# Operadores de comparação
a = input("Digite um valor: ")
b = input("Digite outro valor: ")
print(a == b) # Igual ==
print(a != b) # Diferente !=
print(a > b) # Maior >
print(a < b) # Menor <
print(a >= b) # Maior ou Igual >=
print(a <= b) # Menor ou Igual <=

# Operadores Lógicos
a = input("Digite um valor: ")
b = input("Digite outro valor: ")
print(a and b) # E
print(a or b) # Ou
print(not a) # Nao

# Operadores de atribuição
